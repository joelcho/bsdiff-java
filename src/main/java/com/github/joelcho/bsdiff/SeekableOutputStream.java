package com.github.joelcho.bsdiff;

import java.io.IOException;
import java.io.OutputStream;

/**
 * Seekable {@link OutputStream}
 *
 * @author Joel
 */
public abstract class SeekableOutputStream extends OutputStream implements Seeker {

    /**
     * Returns this stream position.
     * <p>
     * Returns a non-negative integer counting the number of bytes from the beginning to the current position.
     *
     * @return This stream's current position,
     * @throws IOException If some other I/O error occurs
     */
    public abstract long position() throws IOException;
}
